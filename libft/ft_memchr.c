#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned long	i;

	i = 0;
	while (i < n)
	{
		if (*(char *)s != c)
			i++;
		else
			return ((void *)s);
		s++;
	}
	return (NULL);
}
