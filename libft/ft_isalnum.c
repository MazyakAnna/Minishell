#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= '\x41' && c <= '\x5a')
		|| (c > '\x60' && c < '\x7b')
		|| (c >= '\x30' && c <= '\x39'))
		return (1);
	else
		return (0);
}
